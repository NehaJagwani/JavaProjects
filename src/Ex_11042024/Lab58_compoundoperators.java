package Ex_11042024;

public class Lab58_compoundoperators {
    public static void main(String[] args) {
        int i1=10;
        i1+=1;
        System.out.println(i1);

        int i2=10;
        i2-=1;
        System.out.println(i2);

        int i3=10;
        i3*=10;
        System.out.println(i3);

        int i4=10;
        i4/=10;
        System.out.println(i4);

        int i5=20;
        i5%=3;
        System.out.println(i5);
    }
}
