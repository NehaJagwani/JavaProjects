// if \n is assigned to char data type, a new line will be printed and not \n
package Ex_11042024;
public class Lab13_char {
    public static void main(String[] args) {
        char A1='N';
        System.out.printf("The char is %c", A1);

        char A2='\n';
        System.out.println(A2);

        char A3='E';
        System.out.printf("The char is %c", A3);

        char A4='\n';
        System.out.println(A4);

        char A5='H';
        System.out.println(A5);

    }

}
