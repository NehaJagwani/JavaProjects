//equalsIgnoreCase() checks the values of 2 strings ignoring the case and return the boolean value of
//True or False.

package StringFunctions;

public class Lab07_equalsIgnoreCase {
    public static void main(String[] args) {
        String str1="Neha";
        String str2="neha";
        System.out.println(str1.equalsIgnoreCase(str2));

        System.out.println("Raghu".equalsIgnoreCase("raghu"));
    }
}
