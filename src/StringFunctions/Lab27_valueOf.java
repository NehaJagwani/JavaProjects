//valueOf() converts different types of values into string.
//By using valueOf(), we can convert int or long or float or double or char or object or any other type into string.
//valueOf() is a static method, hence, this can be called directly by using String class.

package StringFunctions;

public class Lab27_valueOf {
    public static void main(String[] args) {

        System.out.println(String.valueOf(123));
        System.out.println(String.valueOf("Neha"));
        System.out.println(String.valueOf("I love Peehu the most"));
    }
}
