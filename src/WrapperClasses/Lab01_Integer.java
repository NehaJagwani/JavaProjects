package WrapperClasses;

public class Lab01_Integer {
    public static void main(String[] args) {

        System.out.println(Integer.toString(10));
        System.out.println(Integer.valueOf(20));
        System.out.println(Integer.max(10,100));
        System.out.println(Integer.min(-1,-2));
        System.out.println(Integer.valueOf("120"));
        System.out.println(Integer.compare(10,5));
        System.out.println(Integer.valueOf("123"));
        System.out.println(Integer.valueOf('c'));
    }
}
