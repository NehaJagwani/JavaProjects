//This program will print * in a solid rectangle.

package Star_Patterns;

public class Lab01 {
    public static void main(String[] args) {
        for (int i = 0; i <4 ; i++) {
            for (int j = 0; j <5 ; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
